#include "libft.h"

char	*ft_strndup(const char *s1, size_t n)
{
	char	*s2;

	if (!(s2 = ft_strnew(n)))
		return (NULL);
	s2 = ft_strncat(s2, s1, n);
	s2[n] = '\0';
	return (s2);
}
